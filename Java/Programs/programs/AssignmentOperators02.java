package programs;

public class AssignmentOperators02 {

    public static void main(String[] args) {
        /**
         * = -> Assign 
         * += -> Add Assign
         * -= -> Sub assign
         * *=
         * /=
         * %=
         */

         int a = 10;
         a+=10;
         a = a+10;
         
         System.out.println(a);
    }
    
}
